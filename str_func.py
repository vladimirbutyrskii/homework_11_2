def input_str(str_val: str):
    """
    Вывод текста большими буквами
    :param str_val:
    :return:
    """
    return str_val.upper()


def first_letter_uppercase(str_val: str):
    """
    Преобразование первых букв слов в заглавные
    :param str_val:
    :return:
    """

    return str_val.title()

